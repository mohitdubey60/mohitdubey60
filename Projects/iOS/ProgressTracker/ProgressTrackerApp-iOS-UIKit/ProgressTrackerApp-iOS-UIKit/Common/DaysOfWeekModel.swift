import Foundation

enum DaysOfWeek: String, CaseIterable {
    case sunday     = "SUNDAY"
    case monday     = "MONDAY"
    case tuesday    = "TUESDAY"
    case wednesday  = "WEDNESDAY"
    case thursday   = "THURSDAY"
    case friday     = "FRIDAY"
    case saturday   = "SATURDAY"
}
